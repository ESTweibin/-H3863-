//*****************************************************************************
//! \brief 驱动接口 定义
//*****************************************************************************
#define DRVIER_PORT     SW_DEBUG_UART_BUS

//*****************************************************************************
//! \brief 打印缓冲区尺寸 定义
//*****************************************************************************
#define BUF_SIZE        (128u)

/*
 *  ================================== API ====================================
 */

//*****************************************************************************
//
//! \brief 格式化打印
//!
//! \param [in] fmt 格式化字符串
//! \param [in] ... 参数表
//!
//! \retval - \b 已打印字符数
//
//*****************************************************************************
int32_t kal_printf(const char *fmt, ...)
{
    char     m_buf[BUF_SIZE];
    va_list  m_list;
    uint32_t m_count;

    va_start(m_list, fmt);
    m_count = vsnprintf(m_buf, sizeof(m_buf), fmt, m_list);
    va_end(m_list);

    return (uapi_uart_write(DRVIER_PORT, (const uint8_t *)m_buf, m_count, 0));
}

